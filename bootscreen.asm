
bootscreen_ShowBootScreen:
	push.n {lr}
	push.n {r0, r1, r2, r3}

	bl LCD_GetFrameBuffer
	ldr.n r1, [bootscreen.offlogo]
	adds r0, r0, r1
	ldr.n r1, [bootscreen.logoaddr]

	movs r2, #40

loop:
	ldrb.n r3, [r1]
	strb.n r3, [r0]
	adds r1, r1, #1
	adds r0, r0, #1
	ldrb.n r3, [r1]
	strb.n r3, [r0]
	adds r1, r1, #1
	adds r0, r0, #1
	ldrb.n r3, [r1]
	strb.n r3, [r0]
	adds r1, r1, #1
	adds r0, r0, #16-2
	subs r2, r2, #1
	bne loop

	;bl LCD_SendFrameBuffer

	pop.n {r0, r1, r2, r3}
	pop.n {pc}

align 4

